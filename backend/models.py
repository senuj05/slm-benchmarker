from pydantic import BaseModel
from typing import Optional

class BenchmarkResponse(BaseModel):
    answer: str         # The answer generated by the language model.
    confidence: str     # A confidence score or level indicating the model's certainty about the answer.
    word_count: int     # The number of words in the generated answer.


class ModelMetrics(BaseModel):
    model_name: str
    tokens_per_second: float
    time_to_first_token: float
    memory_used_mb: float
    promt_used: str
    raw_response: str
    